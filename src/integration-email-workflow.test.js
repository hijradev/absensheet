describe('Previous month calculation for scheduled send', () => {
  // Validates: Requirements 4.1 — trigger sends report for the previous month

  it('returns December of previous year when current month is January', () => {
    const jan2024 = new Date(2024, 0, 15); // January 15, 2024
    const result  = calculatePreviousMonth(jan2024);
    expect(result.month).toBe(12);
    expect(result.year).toBe(2023);
    expect(result.monthYear).toBe('2023-12');
  });

  it('returns previous month in same year for February through December', () => {
    const months = [
      { date: new Date(2024, 1, 15), expectedMonth: 1,  expectedYear: 2024, expectedStr: '2024-01' }, // Feb → Jan
      { date: new Date(2024, 2, 15), expectedMonth: 2,  expectedYear: 2024, expectedStr: '2024-02' }, // Mar → Feb
      { date: new Date(2024, 5, 15), expectedMonth: 5,  expectedYear: 2024, expectedStr: '2024-05' }, // Jun → May
      { date: new Date(2024, 11, 15), expectedMonth: 11, expectedYear: 2024, expectedStr: '2024-11' } // Dec → Nov
    ];

    months.forEach(({ date, expectedMonth, expectedYear, expectedStr }) => {
      const result = calculatePreviousMonth(date);
      expect(result.month).toBe(expectedMonth);
      expect(result.year).toBe(expectedYear);
      expect(result.monthYear).toBe(expectedStr);
    });
  });

  it('pads single-digit months with leading zero in monthYear string', () => {
    const march2024 = new Date(2024, 2, 15); // March → February
    const result    = calculatePreviousMonth(march2024);
    expect(result.monthYear).toBe('2024-02');
  });

  it('handles year boundary correctly (January → December previous year)', () => {
    const jan2025 = new Date(2025, 0, 1);
    const result  = calculatePreviousMonth(jan2025);
    expect(result.month).toBe(12);
    expect(result.year).toBe(2024);
    expect(result.monthYear).toBe('2024-12');
  });
});

