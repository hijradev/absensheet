describe('End-to-end email workflow integration', () => {
  // Validates: Requirements 4.1, 4.2, 4.3, 8.1–8.6

  it('complete configuration → trigger → scheduled send workflow', () => {
    // 1. Configure and persist settings
    const emailData = { enabled: true, recipient: 'admin@company.com', scheduleDay: 5, scheduleHour: 9, scheduleMinute: 0 };
    const stored    = simulateSettingsStorage(emailData);
    expect(stored.enabled).toBe(true);

    // 2. Create trigger from stored schedule
    const trigger = simulateTriggerLifecycle(stored.scheduleDay, stored.scheduleHour, stored.scheduleMinute);
    expect(trigger.success).toBe(true);

    // 3. Trigger fires → scheduled send
    const sendResult = simulateScheduledSend(stored, true, true);
    expect(sendResult.success).toBe(true);
    expect(sendResult.log.status).toBe('success');
    expect(sendResult.log.triggerType).toBe('scheduled');
  });

  it('complete configuration → manual send workflow', () => {
    // 1. Configure settings (disabled automatic, but manual should still work)
    const emailData = { enabled: false, recipient: 'admin@company.com', scheduleDay: 5, scheduleHour: 9, scheduleMinute: 0 };
    const stored    = simulateSettingsStorage(emailData);

    // 2. Manual send
    const sendResult = simulateManualSend(stored, true, true);
    expect(sendResult.success).toBe(true);
    expect(sendResult.log.triggerType).toBe('manual');
  });

  it('disabling feature stops scheduled sends but not manual sends', () => {
    const settings = { enabled: false, recipient: 'admin@company.com', scheduleDay: 5, scheduleHour: 9, scheduleMinute: 0 };

    const scheduledResult = simulateScheduledSend(settings, true, true);
    const manualResult    = simulateManualSend(settings, true, true);

    expect(scheduledResult.skipped).toBe(true);
    expect(manualResult.success).toBe(true);
  });

  it('error in one step does not corrupt subsequent log entries', () => {
    const settings = { enabled: true, recipient: 'admin@company.com', scheduleDay: 5, scheduleHour: 9, scheduleMinute: 0 };

    // First send fails
    const failResult = simulateScheduledSend(settings, false, true);
    expect(failResult.log.status).toBe('failed');

    // Second send succeeds — log should be independent
    const successResult = simulateScheduledSend(settings, true, true);
    expect(successResult.log.status).toBe('success');
  });

  it('schedule change updates trigger without breaking email delivery', () => {
    // Original schedule
    const original = simulateTriggerLifecycle(5, 9, 0);
    expect(original.success).toBe(true);

    // Updated schedule (simulates removeEmailScheduleTrigger + setupEmailScheduleTrigger)
    const updated = simulateTriggerLifecycle(15, 14, 30);
    expect(updated.success).toBe(true);
    expect(updated.triggerId).not.toBe(original.triggerId);

    // Email delivery still works with new schedule
    const settings = { enabled: true, recipient: 'admin@company.com', scheduleDay: 15, scheduleHour: 14, scheduleMinute: 30 };
    const sendResult = simulateScheduledSend(settings, true, true);
    expect(sendResult.success).toBe(true);
  });
});
