/** Validate email format — mirrors Settings.gs regex */
function validateEmailFormat(email) {
  if (!email || typeof email !== 'string') return false;
  return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email);
}

/** Validate schedule parameters — mirrors setupEmailScheduleTrigger validation */
function validateSchedule(day, hour, minute) {
  if (typeof day !== 'number' || isNaN(day) || day < 1 || day > 28 || !Number.isInteger(day)) {
    return { valid: false, error: 'Day must be an integer between 1 and 28' };
  }
  if (typeof hour !== 'number' || isNaN(hour) || hour < 0 || hour > 23 || !Number.isInteger(hour)) {
    return { valid: false, error: 'Hour must be an integer between 0 and 23' };
  }
  if (typeof minute !== 'number' || isNaN(minute) || minute < 0 || minute > 59 || !Number.isInteger(minute)) {
    return { valid: false, error: 'Minute must be an integer between 0 and 59' };
  }
  return { valid: true, error: null };
}

/** Simulate PropertiesService round-trip — mirrors getEmailSettings / saveEmailSettings */
function simulateSettingsStorage(emailData) {
  // Store (mirrors saveEmailSettings)
  const stored = {
    MONTHLY_EMAIL_ENABLED:         emailData.enabled ? 'true' : 'false',
    MONTHLY_EMAIL_RECIPIENT:       emailData.recipient || '',
    MONTHLY_EMAIL_SCHEDULE_DAY:    String(emailData.scheduleDay),
    MONTHLY_EMAIL_SCHEDULE_HOUR:   String(emailData.scheduleHour),
    MONTHLY_EMAIL_SCHEDULE_MINUTE: String(emailData.scheduleMinute)
  };

  // Retrieve (mirrors getEmailSettings)
  return {
    enabled:        stored.MONTHLY_EMAIL_ENABLED === 'true',
    recipient:      stored.MONTHLY_EMAIL_RECIPIENT,
    scheduleDay:    parseInt(stored.MONTHLY_EMAIL_SCHEDULE_DAY,    10),
    scheduleHour:   parseInt(stored.MONTHLY_EMAIL_SCHEDULE_HOUR,   10),
    scheduleMinute: parseInt(stored.MONTHLY_EMAIL_SCHEDULE_MINUTE, 10)
  };
}

/** Simulate trigger lifecycle — mirrors setupEmailScheduleTrigger / removeEmailScheduleTrigger */
function simulateTriggerLifecycle(day, hour, minute) {
  const validation = validateSchedule(day, hour, minute);
  if (!validation.valid) {
    return { success: false, triggerId: null, error: validation.error };
  }
  const triggerId = 'trigger_' + day + '_' + hour + '_' + minute;
  return { success: true, triggerId, error: null };
}

/** Simulate previous-month calculation — mirrors handleScheduledEmailSend */
function calculatePreviousMonth(now) {
  let month = now.getMonth(); // 0-11
  let year  = now.getFullYear();

  if (month === 0) {
    // January → previous month is December of previous year
    month = 12;
    year  = year - 1;
  }
  // For any other month, getMonth() (0-indexed) equals the previous month in 1-indexed
  // e.g. getMonth()=3 (April) → previous month = March = 3 (1-indexed)
  const reportMonth = month;
  return {
    month:     reportMonth,
    year,
    monthYear: year + '-' + String(reportMonth).padStart(2, '0')
  };
}

/** Simulate email composition — mirrors sendMonthlyReportEmail */
function composeEmail(recipientEmail, month, year, orgName) {
  const monthNames = [
    'January', 'February', 'March', 'April', 'May', 'June',
    'July', 'August', 'September', 'October', 'November', 'December'
  ];
  const monthName = monthNames[month - 1];
  return {
    to:      recipientEmail,
    subject: 'Monthly Attendance Report - ' + monthName + ' ' + year,
    body:    'Dear Administrator,\n\nPlease find attached the monthly attendance report for ' +
             monthName + ' ' + year + '.\n\n' +
             'The report has been automatically generated by the ' + orgName + ' attendance management system.\n\n' +
             'Best regards,\n' + orgName + ' Attendance System'
  };
}

/** Simulate delivery log entry — mirrors logEmailDelivery */
function createLogEntry(recipient, monthYear, status, triggerType, errorMessage) {
  return {
    timestamp:    new Date().toISOString(),
    recipient,
    monthYear,
    status,
    errorMessage: errorMessage || '',
    triggerType
  };
}

/** Simulate the complete scheduled send workflow */
function simulateScheduledSend(settings, pdfSuccess, emailSuccess) {
  if (!settings.enabled) {
    return { skipped: true, reason: 'Automatic monthly emails are disabled' };
  }

  if (!settings.recipient) {
    const log = createLogEntry('unknown', '', 'failed', 'scheduled', 'No recipient email configured');
    return { success: false, log };
  }

  const now = new Date();
  const { month, year, monthYear } = calculatePreviousMonth(now);

  if (!pdfSuccess) {
    const log = createLogEntry(settings.recipient, monthYear, 'failed', 'scheduled', 'PDF generation failed');
    return { success: false, log };
  }

  if (!emailSuccess) {
    const log = createLogEntry(settings.recipient, monthYear, 'failed', 'scheduled', 'Email delivery failed');
    return { success: false, log };
  }

  const log = createLogEntry(settings.recipient, monthYear, 'success', 'scheduled', '');
  return { success: true, log, month, year, monthYear };
}

/** Simulate the complete manual send workflow */
function simulateManualSend(settings, pdfSuccess, emailSuccess) {
  if (!settings.recipient || !validateEmailFormat(settings.recipient)) {
    return {
      success: false,
      error: settings.recipient
        ? 'Invalid recipient email format. Please update email settings.'
        : 'No recipient email configured. Please configure email settings first.'
    };
  }

  const now = new Date();
  const month = now.getMonth() + 1;
  const year  = now.getFullYear();
  const monthYear = year + '-' + String(month).padStart(2, '0');

  if (!pdfSuccess) {
    const log = createLogEntry(settings.recipient, monthYear, 'failed', 'manual', 'PDF generation failed');
    return { success: false, error: 'Failed to generate PDF report', log };
  }

  if (!emailSuccess) {
    const log = createLogEntry(settings.recipient, monthYear, 'failed', 'manual', 'Email delivery failed');
    return { success: false, error: 'Failed to send email', log };
  }

  const log = createLogEntry(settings.recipient, monthYear, 'success', 'manual', '');
  return { success: true, error: null, log, recipient: settings.recipient, monthYear };
}

