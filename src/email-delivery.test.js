describe('validateEmailParameters() - complete validation', () => {
  // Validates: Requirements 4.1, 4.2, 4.5

  const mockBlob = { type: 'application/pdf' };

  it('accepts all valid parameters', () => {
    const result = validateEmailParameters('admin@company.com', mockBlob, 1, 2024);
    expect(result.valid).toBe(true);
    expect(result.error).toBeNull();
  });

  it('rejects invalid email', () => {
    const result = validateEmailParameters('invalid-email', mockBlob, 1, 2024);
    expect(result.valid).toBe(false);
    expect(result.error).toContain('email');
  });

  it('rejects null blob', () => {
    const result = validateEmailParameters('admin@company.com', null, 1, 2024);
    expect(result.valid).toBe(false);
    expect(result.error).toContain('blob');
  });

  it('rejects invalid month', () => {
    const result = validateEmailParameters('admin@company.com', mockBlob, 13, 2024);
    expect(result.valid).toBe(false);
    expect(result.error).toContain('Month');
  });

  it('rejects invalid year', () => {
    const result = validateEmailParameters('admin@company.com', mockBlob, 1, 1999);
    expect(result.valid).toBe(false);
    expect(result.error).toContain('Year');
  });

  it('validates parameters in order (email first)', () => {
    const result = validateEmailParameters('', mockBlob, 1, 2024);
    expect(result.valid).toBe(false);
    expect(result.error).toContain('email');
  });

  it('validates parameters in order (blob second)', () => {
    const result = validateEmailParameters('admin@company.com', null, 1, 2024);
    expect(result.valid).toBe(false);
    expect(result.error).toContain('blob');
  });

  it('validates parameters in order (month third)', () => {
    const result = validateEmailParameters('admin@company.com', mockBlob, 0, 2024);
    expect(result.valid).toBe(false);
    expect(result.error).toContain('Month');
  });

  it('validates parameters in order (year fourth)', () => {
    const result = validateEmailParameters('admin@company.com', mockBlob, 1, 2101);
    expect(result.valid).toBe(false);
    expect(result.error).toContain('Year');
  });
});

