/**
 * Validate recipient email parameter
 * @param {string} recipientEmail - Email address
 * @returns {{ valid: boolean, error: string|null }}
 */
function validateRecipientEmail(recipientEmail) {
  if (!recipientEmail || typeof recipientEmail !== 'string') {
    return { valid: false, error: 'Recipient email is required and must be a string' };
  }
  
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  if (!emailRegex.test(recipientEmail)) {
    return { valid: false, error: 'Invalid email format' };
  }
  
  return { valid: true, error: null };
}

/**
 * Validate PDF blob parameter
 * @param {any} pdfBlob - PDF blob object
 * @returns {{ valid: boolean, error: string|null }}
 */
function validatePDFBlob(pdfBlob) {
  if (!pdfBlob) {
    return { valid: false, error: 'PDF blob is required' };
  }
  return { valid: true, error: null };
}

/**
 * Validate month parameter for email
 * @param {number} month - Month number (1-12)
 * @returns {{ valid: boolean, error: string|null }}
 */
function validateEmailMonth(month) {
  if (typeof month !== 'number' || isNaN(month) || month < 1 || month > 12) {
    return { valid: false, error: 'Month must be a number between 1 and 12' };
  }
  return { valid: true, error: null };
}

/**
 * Validate year parameter for email
 * @param {number} year - Year (e.g., 2024)
 * @returns {{ valid: boolean, error: string|null }}
 */
function validateEmailYear(year) {
  if (typeof year !== 'number' || isNaN(year) || year < 2000 || year > 2100) {
    return { valid: false, error: 'Year must be a valid year between 2000 and 2100' };
  }
  return { valid: true, error: null };
}

/**
 * Compose email subject line with month and year
 * @param {number} month - Month number (1-12)
 * @param {number} year - Year (e.g., 2024)
 * @returns {string} Email subject line
 */
function composeEmailSubject(month, year) {
  const monthNames = [
    'January', 'February', 'March', 'April', 'May', 'June',
    'July', 'August', 'September', 'October', 'November', 'December'
  ];
  const monthName = monthNames[month - 1];
  return 'Monthly Attendance Report - ' + monthName + ' ' + year;
}

/**
 * Compose email body with explanatory message
 * @param {number} month - Month number (1-12)
 * @param {number} year - Year (e.g., 2024)
 * @param {string} orgName - Organization name
 * @returns {string} Email body text
 */
function composeEmailBody(month, year, orgName) {
  const monthNames = [
    'January', 'February', 'March', 'April', 'May', 'June',
    'July', 'August', 'September', 'October', 'November', 'December'
  ];
  const monthName = monthNames[month - 1];
  
  return 'Dear Administrator,\n\n' +
    'Please find attached the monthly attendance report for ' + monthName + ' ' + year + '.\n\n' +
    'This report contains a comprehensive summary of employee attendance data for the specified period, ' +
    'including on-time arrivals, late arrivals, early departures, and overall attendance statistics.\n\n' +
    'The report has been automatically generated by the ' + orgName + ' attendance management system.\n\n' +
    'If you have any questions or need additional information, please contact your system administrator.\n\n' +
    'Best regards,\n' +
    orgName + ' Attendance System';
}

/**
 * Validate all email parameters
 * @param {string} recipientEmail - Email address
 * @param {any} pdfBlob - PDF blob
 * @param {number} month - Month number (1-12)
 * @param {number} year - Year (e.g., 2024)
 * @returns {{ valid: boolean, error: string|null }}
 */
function validateEmailParameters(recipientEmail, pdfBlob, month, year) {
  const emailValidation = validateRecipientEmail(recipientEmail);
  if (!emailValidation.valid) {
    return emailValidation;
  }
  
  const blobValidation = validatePDFBlob(pdfBlob);
  if (!blobValidation.valid) {
    return blobValidation;
  }
  
  const monthValidation = validateEmailMonth(month);
  if (!monthValidation.valid) {
    return monthValidation;
  }
  
  const yearValidation = validateEmailYear(year);
  if (!yearValidation.valid) {
    return yearValidation;
  }
  
  return { valid: true, error: null };
}

